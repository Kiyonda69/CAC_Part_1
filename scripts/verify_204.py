"""航大思考204 解の一意性検証

問1: A製薬会社の組織再編報告書 (人員計算)
問2: C商事の海外拠点再編報告書 (記述正誤判定)
"""


def verify_q1():
    """問1: 組織再編後の各部門人員数の検証"""
    # 再編前
    研究 = 120
    開発 = 80
    製造 = 200
    営業 = 150
    管理 = 50

    # 異動 (Step 1)
    研究 -= 20; 開発 += 20  # 研究→開発 20名
    製造 -= 15; 研究 += 15  # 製造→研究 15名
    営業 -= 10; 開発 += 10  # 営業→開発 10名
    管理 -= 5; 営業 += 5    # 管理→営業 5名

    # 新規採用 (Step 2)
    研究 += 25
    開発 += 10
    製造 += 30

    # 退職 (Step 3)
    製造 -= 15
    営業 -= 10

    print(f"問1 再編後:")
    print(f"  研究: {研究}名")
    print(f"  開発: {開発}名")
    print(f"  製造: {製造}名")
    print(f"  営業: {営業}名")
    print(f"  管理(経営企画本部): {管理}名")

    assert 研究 == 140
    assert 開発 == 120
    assert 製造 == 200
    assert 営業 == 135
    assert 管理 == 45

    # 選択肢の検証
    options = {
        1: (115, 110, 185, 145),  # 異動のみ
        2: (115, 120, 200, 135),  # 異動+退職
        3: (140, 120, 215, 145),  # 異動+採用
        4: (140, 130, 200, 125),  # 異動方向誤読
        5: (140, 120, 200, 135),  # 正解
    }
    correct = (研究, 開発, 製造, 営業)
    matches = [k for k, v in options.items() if v == correct]
    assert matches == [5], f"正解は(5)のみであるべき: {matches}"
    print("  → 問1 正解: (5)")


def verify_q2():
    """問2: 海外拠点再編後の記述正誤判定"""
    # 再編前
    北米, 欧州, アジア, 中東, 南米 = 80, 60, 120, 30, 10
    オセアニア = 0
    initial_total = 北米 + 欧州 + アジア + 中東 + 南米

    退職計 = 0

    # Step 1: 中東閉鎖
    北米 += 12; 欧州 += 18; 中東 = 0

    # Step 2: 南米縮小
    北米 += 5; 退職計 += 5; 南米 = 0

    # Step 3: アジア→欧州 15名
    アジア -= 15; 欧州 += 15

    # Step 4: オセアニア開設
    北米 -= 22; アジア -= 18; オセアニア = 40

    # Step 5: 新規採用
    北米 += 10; 欧州 += 12; アジア += 20; オセアニア += 5

    # Step 6: 自己都合退職
    北米 -= 8; 欧州 -= 6; アジア -= 12
    退職計 += 8 + 6 + 12

    final_total = 北米 + 欧州 + アジア + オセアニア

    print(f"\n問2 再編後:")
    print(f"  北米: {北米}名 (変化: {北米-80})")
    print(f"  欧州: {欧州}名 (変化: {欧州-60})")
    print(f"  アジア: {アジア}名 (変化: {アジア-120})")
    print(f"  オセアニア: {オセアニア}名 (変化: {オセアニア-0})")
    print(f"  中東: 0名 (変化: {0-30})")
    print(f"  南米: 0名 (変化: {0-10})")
    print(f"  再編前合計: {initial_total}名 / 再編後合計: {final_total}名")
    print(f"  退職計: {退職計}名")

    # 記述ア: 総人員は再編前より16名増加 (316-300=16) → 正
    ア = (final_total - initial_total == 16)

    # 記述イ: 欧州39増加が全拠点中最大 → 誤 (オセアニア45増のほうが大)
    変化 = {"北米": 北米-80, "欧州": 欧州-60, "アジア": アジア-120,
            "オセアニア": オセアニア-0, "中東": -30, "南米": -10}
    イ = (変化["欧州"] == max(変化.values()))

    # 記述ウ: アジアは30名減少 → 誤 (実際は25名減)
    ウ = (アジア - 120 == -30)

    # 記述エ: 退職した社員は合計31名 → 正
    エ = (退職計 == 31)

    print(f"  ア: {ア} (期待: True)")
    print(f"  イ: {イ} (期待: False)")
    print(f"  ウ: {ウ} (期待: False)")
    print(f"  エ: {エ} (期待: True)")

    assert ア is True
    assert イ is False
    assert ウ is False
    assert エ is True

    options = {
        1: ("ア", "イ"),
        2: ("イ", "ウ"),
        3: ("ウ", "エ"),
        4: ("ア", "エ"),  # 正解
        5: ("イ", "エ"),
    }
    correct_set = {k for k, v in [("ア", ア), ("イ", イ), ("ウ", ウ), ("エ", エ)] if v}
    matches = [k for k, v in options.items() if set(v) == correct_set]
    assert matches == [4], f"正解は(4)のみであるべき: {matches}"
    print("  → 問2 正解: (4)")


if __name__ == "__main__":
    verify_q1()
    verify_q2()
    print("\n[OK] 解の一意性を確認しました")
