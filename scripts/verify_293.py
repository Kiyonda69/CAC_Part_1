"""航大思考293 検証コード
資料読取（構成比の資料解釈・公務員試験（高卒）典型）
問1: 輸出額の品目別構成比＋総額（実数計算可能）
問2: 発電量の電源別構成比＋総量の倍率のみ（実数不明・指数計算）
"""

def verify_q1():
    """問1: 構成比と総額から実数を計算し、各記述の真偽を判定"""
    total_2018 = 4000  # 億円
    total_2023 = 5500  # 億円
    # 構成比（%）: 機械類, 化学製品, 食料品, 繊維品, その他
    comp_2018 = {'機械類': 34.0, '化学製品': 22.0, '食料品': 16.0,
                 '繊維品': 10.0, 'その他': 18.0}
    comp_2023 = {'機械類': 38.0, '化学製品': 20.0, '食料品': 15.0,
                 '繊維品': 6.0, 'その他': 21.0}
    assert abs(sum(comp_2018.values()) - 100.0) < 1e-9
    assert abs(sum(comp_2023.values()) - 100.0) < 1e-9

    v18 = {k: total_2018 * p / 100 for k, p in comp_2018.items()}
    v23 = {k: total_2023 * p / 100 for k, p in comp_2023.items()}
    print('問1 実数(2018):', v18)
    print('問1 実数(2023):', v23)

    results = {}
    # (1) 化学製品の輸出額は2018年に比べて2023年は減少している → 880→1100 増加
    results[1] = v23['化学製品'] < v18['化学製品']
    # (2) 2023年の機械類は2018年の1.5倍を上回る → 2090 > 1360*1.5=2040
    results[2] = v23['機械類'] > v18['機械類'] * 1.5
    # (3) 食料品は3割以上増加 → 825 < 640*1.3=832
    results[3] = v23['食料品'] >= v18['食料品'] * 1.3
    # (4) 繊維品の減少額は100億円を上回る → 400-330=70
    results[4] = (v18['繊維品'] - v23['繊維品']) > 100
    # (5) 機械類+化学製品の構成比合計は2018年より2023年の方が低い → 56→58
    results[5] = (comp_2023['機械類'] + comp_2023['化学製品']) < \
                 (comp_2018['機械類'] + comp_2018['化学製品'])

    print('問1 真偽:', results)
    true_opts = [k for k, ok in results.items() if ok]
    assert true_opts == [2], f'問1の真の記述が一意でない: {true_opts}'
    # 罠の際どさ確認
    assert v18['機械類'] * 1.5 == 2040 and v23['機械類'] == 2090
    assert v18['食料品'] * 1.3 == 832 and v23['食料品'] == 825
    print('問1 検証OK: 正解(2)\n')


def verify_q2():
    """問2: 構成比＋総量倍率のみから指数で各記述の真偽を判定"""
    ratio = 1.2  # 2023年の総発電量は2013年の1.2倍
    comp_2013 = {'火力': 62.0, '原子力': 18.0, '水力': 12.0,
                 '太陽光': 3.0, '風力・その他': 5.0}
    comp_2023 = {'火力': 54.0, '原子力': 15.0, '水力': 11.0,
                 '太陽光': 12.0, '風力・その他': 8.0}
    assert abs(sum(comp_2013.values()) - 100.0) < 1e-9
    assert abs(sum(comp_2023.values()) - 100.0) < 1e-9

    # 2013年の総発電量を100とした指数
    idx13 = dict(comp_2013)
    idx23 = {k: p * ratio for k, p in comp_2023.items()}
    print('問2 指数(2013):', idx13)
    print('問2 指数(2023):', {k: round(v, 2) for k, v in idx23.items()})

    results = {}
    # (1) 火力は減少している → 62→64.8 増加
    results[1] = idx23['火力'] < idx13['火力']
    # (2) 2023年の太陽光は2013年の5倍を上回る → 14.4/3=4.8倍
    results[2] = idx23['太陽光'] > idx13['太陽光'] * 5
    # (3) 水力の増加率は火力の増加率より小さい → 水力+10% > 火力+4.5%
    rate_hydro = idx23['水力'] / idx13['水力'] - 1
    rate_thermal = idx23['火力'] / idx13['火力'] - 1
    results[3] = rate_hydro < rate_thermal
    # (4) 2013年の風力・その他は太陽光の2倍を上回る → 5/3≒1.67倍
    results[4] = idx13['風力・その他'] > idx13['太陽光'] * 2
    # (5) 原子力は2013年と2023年で等しい → 18 vs 15*1.2=18
    results[5] = abs(idx23['原子力'] - idx13['原子力']) < 1e-9

    print('問2 真偽:', results)
    print(f'問2 増加率: 水力 {rate_hydro:.3f}, 火力 {rate_thermal:.3f}')
    true_opts = [k for k, ok in results.items() if ok]
    assert true_opts == [5], f'問2の真の記述が一意でない: {true_opts}'
    # 任意の総量Tで実数が成立することの確認（実数不明でも真偽が不変）
    for T in (100, 1234, 98765):
        a13 = comp_2013['原子力'] / 100 * T
        a23 = comp_2023['原子力'] / 100 * T * ratio
        assert abs(a13 - a23) < 1e-6
    print('問2 検証OK: 正解(5)')


if __name__ == '__main__':
    verify_q1()
    verify_q2()
    print('\n全検証OK: 問1正解(2) / 問2正解(5)')
