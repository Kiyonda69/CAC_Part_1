def verify_q1():
    """問1: 喫茶店の売上データから3つの空欄を埋める"""
    # 資料データ
    data = {
        '月': {'客数': 50, '単価': 800},
        '火': {'客数': 45, '単価': 800},
        '水': {'客数': 60, '単価': 900},
        '木': {'客数': 55, '単価': 900},
        '金': {'客数': 80, '単価': 1000},
        '土': {'客数': 120, '単価': 1200},
        '日': {'客数': 110, '単価': 1200},
    }

    # ア: 平日（月～金）の平均客数
    weekday = ['月', '火', '水', '木', '金']
    avg_weekday = sum(data[d]['客数'] for d in weekday) / len(weekday)
    ア = avg_weekday

    # イ: 売上が最大の曜日
    sales = {d: data[d]['客数'] * data[d]['単価'] for d in data}
    イ = max(sales, key=sales.get)

    # ウ: その日の売上
    ウ = sales[イ]

    print(f"問1: ア={ア}, イ={イ}, ウ={ウ}")
    assert ア == 58, f"ア の値が異常: {ア}"
    assert イ == '土', f"イ の値が異常: {イ}"
    assert ウ == 144000, f"ウ の値が異常: {ウ}"

    # 選択肢
    options = {
        1: {'ア': 60, 'イ': '土', 'ウ': 144000},
        2: {'ア': 58, 'イ': '土', 'ウ': 144000},  # 正解
        3: {'ア': 58, 'イ': '日', 'ウ': 132000},
        4: {'ア': 58, 'イ': '土', 'ウ': 132000},
        5: {'ア': 58, 'イ': '金', 'ウ': 80000},
    }

    correct = [k for k, v in options.items()
               if v['ア'] == ア and v['イ'] == イ and v['ウ'] == ウ]
    assert len(correct) == 1, f"解が一意でない: {correct}"
    assert correct[0] == 2, f"正解番号が異なる: {correct[0]}"
    print(f"問1の正解: ({correct[0]}) - 一意性確認OK\n")


def verify_q2():
    """問2: 2社の業績比較から4つの空欄を埋める"""
    # 資料データ
    companies = {
        'A': {'売上': 200, '営業利益': 30, '従業員': 1000},
        'B': {'売上': 120, '営業利益': 24, '従業員': 500},
    }

    # ア: B社の営業利益率(%)
    ア = companies['B']['営業利益'] / companies['B']['売上'] * 100

    # イ: A社の従業員一人当たり売上(万円)
    # 売上は億円なので、1億 = 10000万円
    イ = companies['A']['売上'] * 10000 / companies['A']['従業員']

    # ウ: B社の従業員一人当たり売上(万円)
    ウ = companies['B']['売上'] * 10000 / companies['B']['従業員']

    # エ: 生産性比 B/A
    エ = ウ / イ

    print(f"問2: ア={ア}, イ={イ}, ウ={ウ}, エ={エ}")
    assert ア == 20, f"ア の値が異常: {ア}"
    assert イ == 2000, f"イ の値が異常: {イ}"
    assert ウ == 2400, f"ウ の値が異常: {ウ}"
    assert abs(エ - 1.2) < 0.001, f"エ の値が異常: {エ}"

    # A社の営業利益率の確認（問題文中の値）
    a_profit_rate = companies['A']['営業利益'] / companies['A']['売上'] * 100
    assert a_profit_rate == 15, f"A社の利益率が15%でない: {a_profit_rate}"

    # 選択肢
    options = {
        1: {'ア': 15, 'イ': 2000, 'ウ': 2400, 'エ': 1.2},
        2: {'ア': 20, 'イ': 2000, 'ウ': 2400, 'エ': 1.5},
        3: {'ア': 20, 'イ': 2400, 'ウ': 2000, 'エ': 0.83},
        4: {'ア': 18, 'イ': 2000, 'ウ': 2400, 'エ': 1.2},
        5: {'ア': 20, 'イ': 2000, 'ウ': 2400, 'エ': 1.2},  # 正解
    }

    correct = [k for k, v in options.items()
               if v['ア'] == ア and v['イ'] == イ and v['ウ'] == ウ
               and abs(v['エ'] - エ) < 0.001]
    assert len(correct) == 1, f"解が一意でない: {correct}"
    assert correct[0] == 5, f"正解番号が異なる: {correct[0]}"
    print(f"問2の正解: ({correct[0]}) - 一意性確認OK\n")


if __name__ == '__main__':
    verify_q1()
    verify_q2()
    print("=== 全ての検証に合格 ===")
