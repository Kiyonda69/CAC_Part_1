#!/usr/bin/env python3
"""航大思考292 検証スクリプト（資料解釈・公務員試験（高卒）典型）

問1: 実数表（公共施設の年間利用者数）から確実にいえる記述を1つ特定
問2: 対前年増加率のみの表（製品A〜Dの生産量）から確実にいえる記述を1つ特定
各問とも5つの記述の真偽を計算で判定し、真が唯一であることを確認する。
"""

# ============================================================
# 問1: 公共施設の年間利用者数（単位: 千人）
# ============================================================
DATA1 = {
    "図書館":     {2022: 240, 2023: 264, 2024: 290},
    "体育館":     {2022: 180, 2023: 171, 2024: 190},
    "美術館":     {2022: 120, 2023: 132, 2024: 120},
    "市民ホール": {2022: 100, 2023: 110, 2024: 121},
    "プール":     {2022: 160, 2023: 168, 2024: 180},
}


def totals(year):
    return sum(v[year] for v in DATA1.values())


def verify_q1():
    t22, t23, t24 = totals(2022), totals(2023), totals(2024)
    print(f"総利用者数: 2022={t22}, 2023={t23}, 2024={t24}")
    assert (t22, t23, t24) == (800, 845, 901)

    results = {}

    # (1) 図書館の2024年の対前年増加率は2023年の対前年増加率より大きい
    r23 = DATA1["図書館"][2023] / DATA1["図書館"][2022] - 1  # 10.00%
    r24 = DATA1["図書館"][2024] / DATA1["図書館"][2023] - 1  # 9.85%
    results[1] = r24 > r23
    print(f"(1) 図書館増加率 2023:{r23:.4f} 2024:{r24:.4f} -> {results[1]}")

    # (2) 2022年に対する2024年の増加率が最も大きい施設は図書館
    rates = {k: v[2024] / v[2022] - 1 for k, v in DATA1.items()}
    best = max(rates, key=rates.get)
    results[2] = best == "図書館"
    print(f"(2) 増加率最大: {best} {rates}")

    # (3) 2023年の総利用者数に占める体育館の割合は20%を超えている【正解】
    share = DATA1["体育館"][2023] / t23
    results[3] = share > 0.20
    print(f"(3) 体育館の割合 2023: {share:.4f} -> {results[3]}")

    # (4) 2024年の総利用者数は2022年に比べて13%以上増加している
    growth = t24 / t22 - 1
    results[4] = growth >= 0.13
    print(f"(4) 総利用者数増加率 2022->2024: {growth:.4f} -> {results[4]}")

    # (5) プールの3年間の平均利用者数は170千人を上回っている
    avg = sum(DATA1["プール"].values()) / 3
    results[5] = avg > 170
    print(f"(5) プール平均: {avg:.2f} -> {results[5]}")

    true_opts = [k for k, v in results.items() if v]
    assert true_opts == [3], f"問1: 真の記述が{true_opts}"
    print("問1: 正解(3)のみが真 => OK\n")


# ============================================================
# 問2: 製品A〜Dの生産量の対前年増加率（%）
# ============================================================
RATES = {
    "A": {2021:  5, 2022:  -2, 2023:  4, 2024:  3},
    "B": {2021: -10, 2022: 11, 2023:  5, 2024: -2},
    "C": {2021: 20, 2022: -10, 2023:  2, 2024:  0},
    "D": {2021:  2, 2022:   3, 2023:  3, 2024:  2},
}


def index(prod, year):
    """2020年=100とした指数"""
    v = 100.0
    for y in range(2021, year + 1):
        v *= 1 + RATES[prod][y] / 100
    return v


def verify_q2():
    for p in RATES:
        seq = [round(index(p, y), 3) for y in range(2021, 2025)]
        print(f"製品{p} 指数(2020=100): {seq}")

    results = {}

    # (1) 製品Bの2022年の生産量は2020年の生産量を上回っている
    results[1] = index("B", 2022) > 100  # 99.9 -> False（-10%+11%の罠）
    print(f"(1) B 2022指数: {index('B', 2022):.3f} -> {results[1]}")

    # (2) 2020年に対する2024年の増加率が最も大きいのは製品C
    idx24 = {p: index(p, 2024) for p in RATES}
    best = max(idx24, key=idx24.get)
    results[2] = best == "C"  # 実際はD（率の単純合計ならCが最大に見える罠）
    print(f"(2) 2024指数最大: {best} {[f'{p}:{v:.3f}' for p, v in idx24.items()]}")

    # (3) 製品Cの2023年の生産量は製品Aの2023年の生産量より多い
    # 増加率のみの資料では製品間の実数比較は不可能 -> 確実にはいえない
    results[3] = False
    print("(3) 製品間の実数比較は判断不能 -> False")

    # (4) 2024年の生産量が2020年に比べて10%以上増加している製品は3つ【正解】
    n = sum(1 for p in RATES if index(p, 2024) >= 110)
    results[4] = n == 3  # A:110.23, C:110.16, D:110.38
    print(f"(4) 10%以上増加の製品数: {n} -> {results[4]}")

    # (5) 製品Dの対前年増加率は2021年から2024年まで毎年上昇している
    seq = [RATES["D"][y] for y in range(2021, 2025)]
    results[5] = all(a < b for a, b in zip(seq, seq[1:]))
    print(f"(5) D増加率推移 {seq} -> {results[5]}")

    true_opts = [k for k, v in results.items() if v]
    assert true_opts == [4], f"問2: 真の記述が{true_opts}"
    print("問2: 正解(4)のみが真 => OK")


if __name__ == "__main__":
    verify_q1()
    verify_q2()
    print("\n=== 全検証OK: 各問とも真の記述は唯一 ===")
