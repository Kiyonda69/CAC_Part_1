def verify(question_label, options, expected_answer):
    valid_count = sum(1 for v in options.values() if v["valid"])
    valid_keys = [k for k, v in options.items() if v["valid"]]
    print(f"=== {question_label} ===")
    for k, v in options.items():
        mark = "○" if v["valid"] else "×"
        print(f"  ({k}) {mark}: {v['reason']}")
    assert valid_count == 1, f"{question_label}: 正解が {valid_count} 個ある（一意でない）"
    assert valid_keys[0] == expected_answer, (
        f"{question_label}: 期待される正解 ({expected_answer}) と検証結果 ({valid_keys[0]}) が不一致"
    )
    print(f"  → 解は一意。正解: ({valid_keys[0]})\n")


