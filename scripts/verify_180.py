"""
航大思考180 検証スクリプト（改訂版）

各選択肢が視覚的に区別できるよう、ペンタゴン端面の形状自体を選択肢ごとに変えた設計。

問1（標準難度・3分）: 対称な家型五角柱の展開図
  正面図: 五角形 (0,0),(4,0),(4,3),(2,4),(0,3)  ← 対称な「家」型
  上面図: 4×2 の長方形を x=2 で縦に2分割
  奥行 D = 2

  選択肢端面のペンタゴン形状（各々視覚的に明らかに異なる）:
    (1) 屋根が高い: (0,0),(4,0),(4,3),(2,5),(0,3) 高さ5
    (2) 正解:       (0,0),(4,0),(4,3),(2,4),(0,3) 高さ4
    (3) 非対称:     (0,0),(4,0),(4,3),(1,4),(0,3) 頂点x=1
    (4) 底が広い:   (0,0),(5,0),(5,3),(2.5,4),(0,3) 底5
    (5) 全体が低い: (0,0),(4,0),(4,2),(2,3),(0,2) 高さ3・壁2
  正解: (2)

問2（高難度・5分）: 非対称な家型五角柱の展開図
  正面図: 五角形 (0,0),(6,0),(6,2),(2,4),(0,4)  ← 左が高い非対称
  上面図: 6×3 の長方形を x=2 で縦に2分割
  奥行 D = 3

  選択肢端面のペンタゴン形状（各々視覚的に明らかに異なる）:
    (1) 全体が低い: (0,0),(6,0),(6,2),(2,3),(0,3) 高さ3
    (2) 右壁が長い: (0,0),(6,0),(6,3),(2,4),(0,4) 右壁3
    (3) 正解:       (0,0),(6,0),(6,2),(2,4),(0,4) 右壁2・頂点x=2
    (4) 頂点が右寄り:(0,0),(6,0),(6,2),(4,4),(0,4) 頂点x=4
    (5) 左右反転:   (0,0),(6,0),(6,4),(4,4),(0,2) 右壁が長く頂点が右
  正解: (3)
"""

import math


def pentagon_edges(verts):
    n = len(verts)
    edges = []
    for i in range(n):
        a, b = verts[i], verts[(i + 1) % n]
        L = math.hypot(b[0] - a[0], b[1] - a[1])
        edges.append((a, b, L))
    return edges


def verify_q1():
    print("=" * 60)
    print("問1: 対称な家型五角柱（正解=(2)）")
    print("=" * 60)

    correct = [(0, 0), (4, 0), (4, 3), (2, 4), (0, 3)]
    target_lengths = [4.0, 3.0, math.sqrt(5), math.sqrt(5), 3.0]

    # 各選択肢のペンタゴン
    options = {
        1: [(0, 0), (4, 0), (4, 3), (2, 5), (0, 3)],       # 屋根高い
        2: [(0, 0), (4, 0), (4, 3), (2, 4), (0, 3)],       # 正解
        3: [(0, 0), (4, 0), (4, 3), (1, 4), (0, 3)],       # 非対称（頂点x=1）
        4: [(0, 0), (5, 0), (5, 3), (2.5, 4), (0, 3)],     # 底が広い
        5: [(0, 0), (4, 0), (4, 2), (2, 3), (0, 2)],       # 全体が低い
    }
    descriptions = {
        1: "屋根が高い（高さ5）",
        2: "正解（対称・高さ4）",
        3: "非対称（頂点x=1）",
        4: "底が広い（底5）",
        5: "全体が低い（高さ3）",
    }
    valid = []
    for k, pent in options.items():
        edges = pentagon_edges(pent)
        lengths = [round(L, 4) for _, _, L in edges]
        target = [round(L, 4) for L in target_lengths]
        ok = lengths == target
        # 加えて頂点位置（x=2）も確認
        apex_ok = pent[3][0] == 2
        ok = ok and apex_ok
        print(f"  ({k}) {descriptions[k]} 辺長={lengths} apex_x={pent[3][0]} → {'正解' if ok else '不正解'}")
        if ok:
            valid.append(k)
    assert valid == [2], f"一意解でない: {valid}"
    print(f"\n問1 一意解: ({valid[0]})  [想定: (2)] OK\n")


def verify_q2():
    print("=" * 60)
    print("問2: 非対称な家型五角柱（正解=(3)）")
    print("=" * 60)

    correct = [(0, 0), (6, 0), (6, 2), (2, 4), (0, 4)]
    target_lengths = [6.0, 2.0, math.sqrt(20), 2.0, 4.0]
    target_apex_x = 2

    options = {
        1: [(0, 0), (6, 0), (6, 2), (2, 3), (0, 3)],   # 全体が低い（高さ3）
        2: [(0, 0), (6, 0), (6, 3), (2, 4), (0, 4)],   # 右壁が長い（3）
        3: [(0, 0), (6, 0), (6, 2), (2, 4), (0, 4)],   # 正解
        4: [(0, 0), (6, 0), (6, 2), (4, 4), (0, 4)],   # 頂点が右寄り（x=4）
        5: [(0, 0), (6, 0), (6, 4), (4, 4), (0, 2)],   # 左右反転
    }
    descriptions = {
        1: "全体が低い（高さ3・上面が下がる）",
        2: "右壁が長い（3単位）",
        3: "正解（右壁2・頂点x=2・高さ4）",
        4: "頂点が右寄り（x=4）",
        5: "左右反転（右壁4・頂点が右側）",
    }
    valid = []
    for k, pent in options.items():
        edges = pentagon_edges(pent)
        lengths = [round(L, 4) for _, _, L in edges]
        target = [round(L, 4) for L in target_lengths]
        length_ok = lengths == target
        apex_ok = pent[3][0] == target_apex_x
        # 左右反転判定: 左壁(辺5)が右壁(辺2)より長いことが条件（正解は右壁2<左壁4）
        ok = length_ok and apex_ok
        print(f"  ({k}) {descriptions[k]} 辺長={lengths} apex_x={pent[3][0]} → {'正解' if ok else '不正解'}")
        if ok:
            valid.append(k)
    assert valid == [3], f"一意解でない: {valid}"
    print(f"\n問2 一意解: ({valid[0]})  [想定: (3)] OK\n")


if __name__ == "__main__":
    verify_q1()
    verify_q2()
    print("=" * 60)
    print("全検証 PASS")
