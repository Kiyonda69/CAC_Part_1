"""
航大思考170 検証スクリプト - 資料解釈問題

問1: 5社の四半期売上データから、毎四半期増加かつ年間合計が指定範囲の会社を選ぶ
問2: 売上表と経費表から、複数条件を満たす会社を選ぶ
"""

# ============================================================
# 問1: 単一表の条件抽出
# ============================================================
def verify_q1():
    """問1: Q1→Q4まで毎四半期増加し、かつ年間合計が400以上450以下の会社"""
    # 売上データ（百万円）
    sales = {
        'A': [80, 95, 110, 130],   # 増加、合計415 → 該当
        'B': [120, 100, 90, 110],  # 減少あり
        'C': [60, 75, 95, 120],    # 増加、合計350 → 範囲外
        'D': [90, 110, 105, 115],  # 一時減少
        'E': [100, 120, 140, 130], # 一時減少
    }

    valid = []
    for name, q in sales.items():
        # 条件1: 単調増加
        is_increasing = all(q[i] < q[i+1] for i in range(3))
        # 条件2: 合計が範囲内
        total = sum(q)
        in_range = 400 <= total <= 450

        if is_increasing and in_range:
            valid.append(name)

    print(f"[問1] 該当会社: {valid}")
    print(f"  各社の総売上: {[(n, sum(q)) for n, q in sales.items()]}")
    assert len(valid) == 1, f"解が{len(valid)}個存在"
    return valid[0]


# ============================================================
# 問2: 2表クロスリファレンス
# ============================================================
def verify_q2():
    """
    問2: 以下3条件をすべて満たす会社を選ぶ
      条件1: Q4の利益率（(売上-経費)/売上）が30%以上
      条件2: 売上のQ4がQ1の1.5倍以上
      条件3: 4四半期すべてで利益（売上-経費）が正
    """
    # 売上（百万円）
    sales = {
        'P': [60,  72,  84,  96],   # Q4/Q1=1.6
        'Q': [80,  90, 100, 110],   # Q4/Q1=1.375
        'R': [50,  60,  72,  88],   # Q4/Q1=1.76
        'S': [70,  80,  90,  100],  # Q4/Q1=1.43
        'T': [40,  56,  64,  72],   # Q4/Q1=1.8
    }
    # 経費（百万円）
    costs = {
        'P': [45, 52, 58, 70],   # Q4利益率=(96-70)/96=27.1% → 不可
        'Q': [55, 60, 65, 72],   # Q4/Q1=1.375 → 不可（条件2）
        'R': [38, 44, 50, 58],   # Q4利益率=(88-58)/88=34.1%, Q4/Q1=1.76 → 該当
        'S': [50, 55, 60, 75],   # Q4利益率=(100-75)/100=25% → 不可
        'T': [30, 42, 50, 58],   # Q4利益率=(72-58)/72=19.4% → 不可
    }

    valid = []
    for name in sales:
        s = sales[name]
        c = costs[name]
        # 条件1: Q4の利益率
        q4_profit_rate = (s[3] - c[3]) / s[3]
        cond1 = q4_profit_rate >= 0.30
        # 条件2: Q4/Q1
        cond2 = s[3] / s[0] >= 1.5
        # 条件3: 全四半期で利益が正
        cond3 = all(s[i] - c[i] > 0 for i in range(4))

        print(f"  {name}: 利益率={q4_profit_rate*100:.1f}%, "
              f"Q4/Q1={s[3]/s[0]:.3f}, "
              f"全期黒字={cond3}")

        if cond1 and cond2 and cond3:
            valid.append(name)

    print(f"[問2] 該当会社: {valid}")
    assert len(valid) == 1, f"解が{len(valid)}個存在"
    return valid[0]


if __name__ == '__main__':
    print("=" * 50)
    print("航大思考170 検証")
    print("=" * 50)
    ans1 = verify_q1()
    print()
    ans2 = verify_q2()
    print()
    print(f"問1の正解: {ans1}")
    print(f"問2の正解: {ans2}")
