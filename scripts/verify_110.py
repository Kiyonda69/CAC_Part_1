"""
verify_110.py - 航大思考110 の解の一意性検証

問1: 着陸進入時の滑走路の見え方
  - 飛行機から滑走路を見たとき、滑走路（地面の長方形）は
    上辺が狭く下辺が広い「台形」に見える。
  - 直進して着陸するまで、台形は相似的に大きくなるだけで、
    上辺 < 下辺 の関係は常に保たれる。
  - 「上辺 >= 下辺」となる選択肢が誤り。

問2: 進入中の地平線の位置
  - ピッチを一定に保って直進（降下）する場合、視線の水平方向は
    常に地平線に一致するため、フレーム内の地平線の見かけの高さは変わらない。
  - 台形（滑走路）は相似的に成長するが、台形の上辺は常に地平線上にあり、
    地平線の高さ自体は移動しない。
  - 「地平線の高さがフレーム内で移動する」選択肢が誤り。
"""


def verify_q1():
    """問1: 上辺 < 下辺 を満たさない選択肢を特定"""
    # (top_width, bottom_width) 単位: px（相対値）
    options = {
        1: (40, 90),    # 通常の拡大（進入中）
        2: (55, 120),   # さらに拡大
        3: (70, 150),   # 接地直前
        4: (30, 70),    # やや拡大（直後）
        5: (120, 50),   # 逆台形（上が広く下が狭い） ← 誤り
    }

    invalid = [i for i, (t, b) in options.items() if t >= b]
    assert len(invalid) == 1, f"誤りの選択肢が {len(invalid)} 個: {invalid}"
    assert invalid[0] == 5, f"問1 の誤り選択肢は (5) のはずだが {invalid[0]}"
    return invalid[0]


def verify_q2():
    """問2: 地平線がフレーム内で移動する選択肢を特定

    仕様:
      - フレーム高さ: 100（相対）
      - 地平線の高さ（フレーム下端から）: 一定値 50 であるべき
      - 台形の上辺は常に地平線上（y = 50）
      - 台形の下辺は選択肢ごとに拡大（フレーム下端に近づく）
    """
    # (horizon_y, top_y, bottom_y): フレーム下端からの高さ
    # 期待値: horizon_y == top_y == 50（常に）、bottom_y が小さいほど接地に近い
    options = {
        1: (50, 50, 30),   # 少し拡大
        2: (50, 50, 18),   # さらに拡大
        3: (35, 35, 10),   # 地平線が下がっている（誤り）
        4: (50, 50, 8),    # 接地直前
        5: (50, 50, 22),   # 中程度
    }

    invalid = [i for i, (h, t, b) in options.items() if h != 50 or t != h]
    assert len(invalid) == 1, f"誤りの選択肢が {len(invalid)} 個: {invalid}"
    assert invalid[0] == 3, f"問2 の誤り選択肢は (3) のはずだが {invalid[0]}"

    # また、正しい選択肢は全て top_y < bottom_y の関係（台形が逆転していない）
    for i, (h, t, b) in options.items():
        if i == 3:
            continue
        assert t > b, f"選択肢 ({i}) の上辺/下辺が不正"

    return invalid[0]


if __name__ == "__main__":
    ans1 = verify_q1()
    ans2 = verify_q2()
    print(f"問1 正解: ({ans1})")
    print(f"問2 正解: ({ans2})")
    print("検証成功: 各問で誤りの選択肢が唯一")
